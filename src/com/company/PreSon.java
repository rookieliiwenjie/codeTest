public class PreSon {
    public String userName;
    public Long id;
    public String name;

    public PreSon() {
        this.userName = userName;
        this.id = id;
        this.name = name;
    }

    @Override
    public String toString() {
        return "PreSon{" +
                "userName='" + userName + '\'' +
                ", id=" + id +
                ", name='" + name + '\'' +
                '}';
    }
}
